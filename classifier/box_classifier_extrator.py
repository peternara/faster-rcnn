import tensorflow as tf
import tensorflow.contrib.slim as slim


def extract(roi_feature_maps, scope='box_classifier_feature_extrator'):
    with tf.variable_scope(scope, 'box_classifier_feature_extrator', [roi_feature_maps]):
        with slim.arg_scope([slim.max_pool2d], padding='SAME'):
            net = slim.repeat(roi_feature_maps, 2, slim.conv2d, 64, [3, 3], scope='conv1')
            net = slim.max_pool2d(net, [2, 2], scope='pool1')
            net = slim.repeat(net, 2, slim.conv2d, 128, [3, 3], scope='conv2')
            net = slim.max_pool2d(net, [2, 2], scope='pool2')
            net = slim.repeat(net, 3, slim.conv2d, 256, [3, 3], scope='conv3')
            net = slim.max_pool2d(net, [2, 2], scope='pool3')
            net = slim.repeat(net, 3, slim.conv2d, 512, [3, 3], scope='conv4')
            net = slim.max_pool2d(net, [2, 2], scope='pool4')
            net = slim.repeat(net, 3, slim.conv2d, 512, [3, 3], scope='conv5')
            return net
